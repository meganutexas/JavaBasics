public class TypesAndVariables {
    public static void main(String[] args) {
        int age = 25;
        System.out.println(
                "I am " + age + " years old"
        );

        double costOfLastMeal = 5.78;
        System.out.println(
                "My last meal cost: " + costOfLastMeal
        );

        boolean hasPets = false;
        System.out.println(
                "I have pets? " + hasPets
        );

        char favoriteNumber = '7';
        System.out.println(
                "My favorite number is: " + favoriteNumber
        );
    }
}
